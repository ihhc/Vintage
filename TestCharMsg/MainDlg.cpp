BOOL CMainDlg::PreTranslateMessage(MSG* pMsg)
{
	if (pMsg->message == WM_KEYDOWN)
	{
		char ch[2];
		ch[0] = pMsg->wParam; 
		ch[1] = '\0';
		MessageBox(ch, "key is");
	}
	if (pMsg->message == WM_CHAR)
	{
		char ch[2];
		ch[0] = pMsg->wParam; 
		ch[1] = '\0';
		MessageBox(ch, "char is");
	}
	return CWindow::IsDialogMessage(pMsg);
}

